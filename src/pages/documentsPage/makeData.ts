export interface Document {
  id: number;
  номер: string;
  дата: string;
  комментарий: string;
  тип: 'приходная' | 'расходная' | 'инвентаризация' | 'списание' | 'перемещение' | 'отчёт';
  строки?: DocumentLine[]; // Добавляем строки документа
}

export type DocumentFormData = Omit<Document, 'id'>;

// Интерфейс для строки документа
export interface DocumentLine {
  id: number;
  documentId: number;
  товарId: number;
  артикул: string;
  наименование: string;
  ценаЗакупки: number;
  ценаПродажи: number;
  количество: number; // Количество по учету
  фактическоеКоличество?: number; // Только для инвентаризации/списания
  единицаИзмерения: string;
  категория: string;
  сумма: number;
}

